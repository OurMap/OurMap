function msg_toggle(div_id) {
	var el = document.getElementById(div_id);
	if ( el.style.display == 'none' ) {	el.style.display = 'block';}
	else {el.style.display = 'none';}
}
function trans_blanket_size(popUpDivVar) {

	if (typeof window.innerWidth != 'undefined') {
		viewportheight = window.innerHeight;
	} else {
		viewportheight = document.documentElement.clientHeight;
	}
	if ((viewportheight > document.body.parentNode.scrollHeight) && (viewportheight > document.body.parentNode.clientHeight)) {
		blanket_height = viewportheight;
	} else {
		if (document.body.parentNode.clientHeight > document.body.parentNode.scrollHeight) {
			blanket_height = document.body.parentNode.clientHeight;
		} else {
			blanket_height = document.body.parentNode.scrollHeight;
		}
	}
	var blanket = document.getElementById('transparent_blanket');
	blanket.style.height = blanket_height + 'px';

	var popUpDiv = document.getElementById(popUpDivVar);
	popUpDiv_height=blanket_height/2-150;//150 is half popup's height
	popUpDiv.style.top = popUpDiv_height + 'px';
}


function msg_window_pos(popUpDivVar) {
    

	if (typeof window.innerWidth != 'undefined') {
		viewportwidth = window.innerHeight;
	} else {
		viewportwidth = document.documentElement.clientHeight;
	}
	if ((viewportwidth > document.body.parentNode.scrollWidth) && (viewportwidth > document.body.parentNode.clientWidth)) {
		window_width = viewportwidth;
	} else {
		if (document.body.parentNode.clientWidth > document.body.parentNode.scrollWidth) {
			window_width = document.body.parentNode.clientWidth;
		} else {
			window_width = document.body.parentNode.scrollWidth;
		}
	}
        
	var popUpDiv = document.getElementById(popUpDivVar);

	//window_width = window_width/2-400;//150 is half popup's width

        var width = window_width;
        var popupWidth = 800;
        var offsetLeft = 90 ;
        var left = offsetLeft + width - ( width + popupWidth) / 2 ;
        var top = 200;

	popUpDiv.style.left = left + 'px';
	popUpDiv.style.top = top + 'px';

}

function popupMessage(windowname) {
	trans_blanket_size(windowname);
	msg_window_pos(windowname);
	msg_toggle('transparent_blanket');
	msg_toggle(windowname);
        
}